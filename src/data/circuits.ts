/**
 * Circuit data with ASCII art track maps for all 23 circuits on the
 * 2026 Formula 1 calendar. Each circuit_short_name matches the value
 * returned by the OpenF1 API so we can cross-reference meetings.
 */

export interface Circuit {
  /** The circuit_short_name from the API (used for matching). */
  name: string;
  /** Full circuit name (e.g. "Albert Park Circuit"). */
  fullName: string;
  /** Grand Prix name (e.g. "Australian Grand Prix"). */
  gpName: string;
  /** City, country. */
  location: string;
  /** Track length in kilometers. */
  lengthKm: number;
  /** Number of corners. */
  turns: number;
  /** Lap record string, or "N/A" if unknown. */
  lapRecord: string;
  /** Year the first GP was held at this circuit. */
  firstHeld: number;
  /** "Clockwise" or "Anti-clockwise". */
  direction: 'Clockwise' | 'Anti-clockwise';
  /** ASCII art map, one string per line. */
  map: string[];
  /** [lineIndex, charIndex] of the start/finish line on the map. */
  startLine: [number, number];
}

